/* ==================== Framebuffer Operations ==================== */

/** @brief 清空帧缓冲区并立即刷新到屏幕 */
void OLED_Clear(void) {
    memset(OLED_Buffer, 0x00, sizeof(OLED_Buffer));
    OLED_Update();
}

/** @brief 仅将帧缓冲区全部置零，不发送到屏幕
 *  通常在绘制新帧前调用，绘制完后调用 OLED_Update() 刷新显示
 */
void OLED_ClearBuffer(void) {
    memset(OLED_Buffer, 0x00, sizeof(OLED_Buffer));
}

/** @brief 将帧缓冲区逐页发送到SSD1306
 *  使用页寻址模式：依次发送 Page 0~7，每页128字节
 *  先发页地址(0xB0|page)，再发列地址(0x00, 0x10)，最后发送该页数据
 */
void OLED_Update(void) {
    /* Page addressing mode for reliable transmission */
    for (uint8_t page = 0; page < SSD1306_PAGES; page++) {
        uint8_t cmds[] = {
            0xB0 | page,   /* Set page address */
            0x00,          /* Set lower column start (2 for some margin, use 0 normally) */
            0x10           /* Set higher column start */
        };
        SSD1306_WriteCommands(cmds, 3);
        SSD1306_WriteData(&OLED_Buffer[page * SSD1306_WIDTH], SSD1306_WIDTH);
    }
}

/** @brief 局部刷新指定页范围（用于提高刷新效率）
 *  @param start_page 起始页(0~7)
 *  @param end_page   结束页(0~7)，仅刷新 [start_page, end_page]
 */
void OLED_UpdateArea(uint8_t start_page, uint8_t end_page) {
    for (uint8_t page = start_page; page <= end_page; page++) {
        uint8_t cmds[] = { 0xB0 | page, 0x00, 0x10 };
        SSD1306_WriteCommands(cmds, 3);
        SSD1306_WriteData(&OLED_Buffer[page * SSD1306_WIDTH], SSD1306_WIDTH);
    }
}

/* ==================== Drawing Primitives ==================== */

/** @brief 在帧缓冲区中设置/擦除单个像素
 *  坐标超出屏幕范围(128x64)时自动忽略
 *  帧缓冲按页组织：Byte[y/8 * 128 + x] 的第 (y%8) 位
 */
void OLED_SetPixel(uint8_t x, uint8_t y, uint8_t color) {
    if (x >= SSD1306_WIDTH || y >= SSD1306_HEIGHT) return;
    if (color)
        OLED_Buffer[x + (y / 8) * SSD1306_WIDTH] |= (1 << (y & 7));
    else
        OLED_Buffer[x + (y / 8) * SSD1306_WIDTH] &= ~(1 << (y & 7));
}

/** @brief 绘制水平直线（逐像素填充，w=0时不绘制）
 *  @param x 起点x坐标
 *  @param y y坐标
 *  @param w 线段宽度
 */
void OLED_DrawHLine(uint8_t x, uint8_t y, uint8_t w) {
    for (uint8_t i = 0; i < w; i++) OLED_SetPixel(x + i, y, 1);
}

/** @brief 绘制垂直直线（逐像素填充，h=0时不绘制）
 *  @param x x坐标
 *  @param y 起点y坐标
 *  @param h 线段高度
 */
void OLED_DrawVLine(uint8_t x, uint8_t y, uint8_t h) {
    for (uint8_t i = 0; i < h; i++) OLED_SetPixel(x, y + i, 1);
}

/** @brief 使用 Bresenham 直线算法绘制任意角度直线
 *  支持8个方向的增量步进，保证像素连续性
 *  @param x0,y0 起点坐标
 *  @param x1,y1 终点坐标
 */
void OLED_DrawLine(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1) {
    int16_t dx = (int16_t)x1 - x0;
    int16_t dy = (int16_t)y1 - y0;
    int16_t sx = dx > 0 ? 1 : -1;
    int16_t sy = dy > 0 ? 1 : -1;
    dx = dx > 0 ? dx : -dx;
    dy = dy > 0 ? dy : -dy;
    int16_t err = dx - dy;

    while (1) {
        OLED_SetPixel(x0, y0, 1);
        if (x0 == x1 && y0 == y1) break;
        int16_t e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x0 += sx; }
        if (e2 < dx)  { err += dx; y0 += sy; }
    }
}

/** @brief 绘制空心矩形边框（1像素线宽）
 *  @param x,y 左上角坐标
 *  @param w   矩形宽度
 *  @param h   矩形高度
 */
void OLED_DrawRect(uint8_t x, uint8_t y, uint8_t w, uint8_t h) {
    OLED_DrawHLine(x, y, w);
    OLED_DrawHLine(x, y + h - 1, w);
    OLED_DrawVLine(x, y, h);
    OLED_DrawVLine(x + w - 1, y, h);
}

/** @brief 绘制实心填充矩形
 *  逐行调用水平线填充，效率低于直接字节操作但实现简洁
 *  @param x,y 左上角坐标
 *  @param w   矩形宽度
 *  @param h   矩形高度
 */
void OLED_DrawFilledRect(uint8_t x, uint8_t y, uint8_t w, uint8_t h) {
    for (uint8_t i = 0; i < h; i++)
        OLED_DrawHLine(x, y + i, w);
}

/** @brief 使用 Bresenham 中点圆算法绘制空心圆
 *  利用八对称性同时绘制8个点，每次迭代只计算1/8圆弧
 *  @param cx,cy 圆心坐标
 *  @param r     半径(像素)
 */
void OLED_DrawCircle(uint8_t cx, uint8_t cy, uint8_t r) {
    int16_t x = 0, y = r;
    int16_t d = 3 - 2 * r;
    while (y >= x) {
        OLED_SetPixel(cx + x, cy + y, 1);
        OLED_SetPixel(cx - x, cy + y, 1);
        OLED_SetPixel(cx + x, cy - y, 1);
        OLED_SetPixel(cx - x, cy - y, 1);
        OLED_SetPixel(cx + y, cy + x, 1);
        OLED_SetPixel(cx - y, cy + x, 1);
        OLED_SetPixel(cx + y, cy - x, 1);
        OLED_SetPixel(cx - y, cy - x, 1);
        x++;
        if (d > 0) { y--; d += 4 * (x - y) + 10; }
        else        { d += 4 * x + 6; }
    }
}

/** @brief 使用中点圆算法绘制实心填充圆
 *  每次迭代画4条水平线填充圆的上下两个"切片"
 *  @param cx,cy 圆心坐标
 *  @param r     半径(像素)
 */
void OLED_DrawFilledCircle(uint8_t cx, uint8_t cy, uint8_t r) {
    int16_t x = 0, y = r;
    int16_t d = 3 - 2 * r;
    while (y >= x) {
        OLED_DrawHLine(cx - x, cy - y, 2 * x + 1);
        OLED_DrawHLine(cx - x, cy + y, 2 * x + 1);
        OLED_DrawHLine(cx - y, cy - x, 2 * y + 1);
        OLED_DrawHLine(cx - y, cy + x, 2 * y + 1);
        x++;
        if (d > 0) { y--; d += 4 * (x - y) + 10; }
        else        { d += 4 * x + 6; }
    }
}

/** @brief 在帧缓冲区中绘制位图（逐像素解析）
 *  位图按页排列：bitmap[col + (row/8) * w] 的第(row%8)位控制像素
 *  @param x,y     位图左上角坐标
 *  @param bitmap  位图数据指针
 *  @param w       位图宽度
 *  @param h       位图高度
 */
void OLED_DrawBitmap(uint8_t x, uint8_t y, const uint8_t *bitmap, uint8_t w, uint8_t h) {
    for (uint8_t j = 0; j < h; j++) {
        for (uint8_t i = 0; i < w; i++) {
            if (bitmap[i + (j / 8) * w] & (1 << (j & 7)))
                OLED_SetPixel(x + i, y + j, 1);
        }
    }
}

/* ==================== Text Rendering (6x8 font) ==================== */

/** @brief 在帧缓冲区中绘制6x8单字符（直接覆盖对应字节，不读取原有内容）
 *  字符仅支持ASCII 0x20~0x7E（共95个可打印字符），超出范围显示空格
 *  @param x    水平像素坐标(0~122)
 *  @param page 页号(0~7)，超出不绘制
 *  @param c    ASCII字符
 */
void OLED_DrawChar6x8(uint8_t x, uint8_t page, char c) {
    if (page >= SSD1306_PAGES) return;
    if (c < 0x20 || c > 0x7E) c = ' ';

    const uint8_t *glyph = Font6x8[(uint8_t)(c - 0x20)];
    for (uint8_t col = 0; col < 6; col++) {
        if (x + col < SSD1306_WIDTH)
            OLED_Buffer[x + col + page * SSD1306_WIDTH] = glyph[col];
    }
}

/** @brief 在帧缓冲区中绘制6x8字符串
 *  字符间距6像素，超出屏幕宽度(128)自动截断
 *  @param x    起始水平坐标
 *  @param page 页号(0~7)
 *  @param str  以'\0'结尾的ASCII字符串
 */
void OLED_DrawString6x8(uint8_t x, uint8_t page, const char *str) {
    while (*str) {
        OLED_DrawChar6x8(x, page, *str);
        x += 6;
        if (x + 6 > SSD1306_WIDTH) break;
        str++;
    }
}

/* ==================== Text Rendering (8x16 font) ==================== */

/** @brief 将ASCII字符映射到 Font8x16 字库索引
 *  映射规则：0-9→0~9, ':'→10, '.'→11, '-'→12, '/'→13, 空格→14, A-Z/a-z→15~40
 *  小写字母自动映射到大写对应位置
 *  @return 字库索引(0~41)，未匹配字符默认返回14(空格)
 */
static uint8_t _char_index_8x16(char c) {
    if (c >= '0' && c <= '9') return c - '0';
    if (c == ':') return 10;
    if (c == '.') return 11;
    if (c == '-') return 12;
    if (c == '/') return 13;
    if (c == ' ') return 14;
    if (c >= 'A' && c <= 'Z') return 15 + (c - 'A');
    if (c >= 'a' && c <= 'z') return 15 + (c - 'a');  /* lowercase mapped to uppercase */
    return 14; /* default: space */
}

/** @brief 在帧缓冲区中绘制8x16单字符（占用2页）
 *  前8字节写当前页，后8字节写下一页
 *  @param x    水平像素坐标(0~120)
 *  @param page 起始页号(0~6)
 *  @param c    要显示的字符（0-9、A-Z/a-z、: . - / 空格）
 */
void OLED_DrawChar8x16(uint8_t x, uint8_t page, char c) {
    if (page + 1 >= SSD1306_PAGES) return;
    if (x + 8 > SSD1306_WIDTH) return;

    uint8_t idx = _char_index_8x16(c);
    const uint8_t *glyph = Font8x16[idx];

    for (uint8_t col = 0; col < 8; col++) {
        OLED_Buffer[x + col + page * SSD1306_WIDTH] = glyph[col];
        OLED_Buffer[x + col + (page + 1) * SSD1306_WIDTH] = glyph[col + 8];
    }
}

/** @brief 在帧缓冲区中绘制8x16字符串
 *  字符间距8像素，超出屏幕宽度自动截断
 *  @param x    起始水平坐标
 *  @param page 起始页号(0~6)
 *  @param str  以'\0'结尾的字符串
 */
void OLED_DrawString8x16(uint8_t x, uint8_t page, const char *str) {
    while (*str) {
        OLED_DrawChar8x16(x, page, *str);
        x += 8;
        if (x + 8 > SSD1306_WIDTH) break;
        str++;
    }
}

/* ==================== Large Number (16x24) Rendering ==================== */

/** @brief 绘制16x24大号数字（用于时钟等大字体场景）
 *  字形48字节，分3页(16列×3页)，支持非页对齐的y坐标
 *  非页对齐时：低位 (data << bit_shift) 写当前字节，高位 (data >> (8-bit_shift)) 写下一行
 *  @param x     水平像素坐标(0~111)
 *  @param y     垂直像素坐标(0~40)，可传非8的倍数
 *  @param digit 数字(0~9)，超出范围不绘制
 */
void OLED_DrawDigit16x24(uint8_t x, uint8_t y, uint8_t digit) {
    if (digit > 9) return;
    if (x + 16 > SSD1306_WIDTH || y + 24 > SSD1306_HEIGHT) return;

    const uint8_t *glyph = Font16x24[digit];
    uint8_t start_page = y / 8;

    for (uint8_t page = 0; page < 3; page++) {
        if (start_page + page >= SSD1306_PAGES) break;
        for (uint8_t col = 0; col < 16; col++) {
            uint16_t offset = x + col + (start_page + page) * SSD1306_WIDTH;
            if (offset < sizeof(OLED_Buffer)) {
                uint8_t data = glyph[page * 16 + col];
                uint8_t bit_shift = y & 7;
                if (bit_shift) {
                    OLED_Buffer[offset] |= (data << bit_shift);
                    if (offset + SSD1306_WIDTH < sizeof(OLED_Buffer))
                        OLED_Buffer[offset + SSD1306_WIDTH] |= (data >> (8 - bit_shift));
                } else {
                    OLED_Buffer[offset] = data;
                }
            }
        }
    }
}

/** @brief 绘制16x24大号冒号（':'）
 *  实现逻辑与 OLED_DrawDigit16x24 相同，使用 Font16x24[10] 字形
 *  @param x 水平像素坐标
 *  @param y 垂直像素坐标
 */
void OLED_DrawColon16x24(uint8_t x, uint8_t y) {
    if (x + 16 > SSD1306_WIDTH || y + 24 > SSD1306_HEIGHT) return;

    const uint8_t *glyph = Font16x24[10]; /* colon */
    uint8_t start_page = y / 8;

    for (uint8_t page = 0; page < 3; page++) {
        if (start_page + page >= SSD1306_PAGES) break;
        for (uint8_t col = 0; col < 16; col++) {
            uint16_t offset = x + col + (start_page + page) * SSD1306_WIDTH;
            if (offset < sizeof(OLED_Buffer)) {
                uint8_t data = glyph[page * 16 + col];
                uint8_t bit_shift = y & 7;
                if (bit_shift) {
                    OLED_Buffer[offset] |= (data << bit_shift);
                    if (offset + SSD1306_WIDTH < sizeof(OLED_Buffer))
                        OLED_Buffer[offset + SSD1306_WIDTH] |= (data >> (8 - bit_shift));
                } else {
                    OLED_Buffer[offset] = data;
                }
            }
        }
    }
}

/** @brief 快速绘制 HH:MM 格式时钟界面（5个16x24字符）
 *  布局：x → HH十位(0~17) → HH个位(18~35) → 冒号(36~53) → MM十位(54~71) → MM个位(72~89)
 *  @param x      起始水平坐标
 *  @param y      垂直像素坐标
 *  @param hour   小时(0~23)
 *  @param minute 分钟(0~59)
 */
void OLED_DrawTime16x24(uint8_t x, uint8_t y, uint8_t hour, uint8_t minute) {
    OLED_DrawDigit16x24(x,       y, hour / 10);
    OLED_DrawDigit16x24(x + 18,  y, hour % 10);
    OLED_DrawColon16x24(x + 36,  y);
    OLED_DrawDigit16x24(x + 54,  y, minute / 10);
    OLED_DrawDigit16x24(x + 72,  y, minute % 10);
}

/* ==================== printf-style output ==================== */

/** @brief 格式化字符串输出（类printf），使用6x8字体渲染
 *  内部使用 vsnprintf 格式化到32字节缓冲区（超出部分会被截断）
 *  @param x    起始水平坐标
 *  @param page 页号(0~7)
 *  @param fmt  printf格式字符串
 *  @param ...  可变参数（与printf用法一致）
 */
void OLED_Printf6x8(uint8_t x, uint8_t page, const char *fmt, ...) {
    char buf[32];
    va_list args;
    va_start(args, fmt);
    vsnprintf(buf, sizeof(buf), fmt, args);
    va_end(args);
    OLED_DrawString6x8(x, page, buf);
}
